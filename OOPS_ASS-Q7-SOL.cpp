/*7. Which of the following statements is correct about the formal parameters
in C++?
a) Parameters with which functions are called
b) Parameters which are used in the definition of the function
c) Variables other than passed parameters in a function
d) Variables that are never used in the function.

---->

The correct statement about formal parameters in C++ is:
b) Parameters which are used in the definition of the function

Explanation:
Formal parameters are the variables that appear in the function's definition. They act as placeholders for the actual values (arguments) that will be passed to the function when it is called.*/
